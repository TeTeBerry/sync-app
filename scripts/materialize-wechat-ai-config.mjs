#!/usr/bin/env node
/**
 * Materialize packageAgentSkills/shared/wechatAiBuildConfig.json from root .env
 * (skill subpackage is plain JS copy — no Taro process.env injection).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const envPath = path.join(root, '.env');
const outJsonPath = path.join(
  root,
  'packageAgentSkills/shared/wechatAiBuildConfig.json',
);
const outJsPath = path.join(root, 'packageAgentSkills/shared/wechatAiBuildConfig.js');

function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) {
    return {};
  }
  const vars = {};
  for (const line of fs.readFileSync(filePath, 'utf-8').split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const idx = trimmed.indexOf('=');
    if (idx < 1) continue;
    const key = trimmed.slice(0, idx).trim();
    let value = trimmed.slice(idx + 1).trim();
    const hashIdx = value.indexOf(' #');
    if (hashIdx >= 0) value = value.slice(0, hashIdx).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    vars[key] = value;
  }
  return vars;
}

const env = parseEnvFile(envPath);
const productionEnv = parseEnvFile(path.join(root, '.env.production'));
const templateId =
  env.TARO_APP_SUBSCRIBE_TMPL_ACTIVITY_UPDATE ||
  productionEnv.TARO_APP_SUBSCRIBE_TMPL_ACTIVITY_UPDATE ||
  '';

const payload = {
  activityUpdateSubscribeTemplateId: templateId,
};

fs.mkdirSync(path.dirname(outJsonPath), { recursive: true });
fs.writeFileSync(outJsonPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf-8');
fs.writeFileSync(
  outJsPath,
  `/** Auto-generated by scripts/materialize-wechat-ai-config.mjs — do not edit */\nmodule.exports = ${JSON.stringify(payload, null, 2)};\n`,
  'utf-8',
);
console.log(
  `[materialize-wechat-ai-config] wrote ${outJsonPath} activityUpdateSubscribeTemplateId=${templateId ? 'set' : 'empty'}`,
);
